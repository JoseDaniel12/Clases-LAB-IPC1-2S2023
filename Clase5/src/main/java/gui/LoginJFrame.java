    private void ingesarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingesarButtonActionPerformed
        String nombre = usuarioTextField.getText();
        String contrasenia = contraseniaTextField.getText();
        
        for (Usuario u: AppState.usuarios) {
            if (nombre.equals(u.nombreUsuario) && contrasenia.equals(u.contrasenia)) {
                AppState.usuarioLogeado = u;
                if (u instanceof Profesor) {
                    ProfesorMainFrame profesorMainFrame = new ProfesorMainFrame();
                } else if (u instanceof Estudiante) {
                    EstudianteMainFrame estudianteMainFrame = new EstudianteMainFrame();
                }
                this.dispose();
                return;
            }
        }
        JOptionPane.showMessageDialog(this, "Credenciales incorrectas", "Alerta", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_ingesarButtonActionPerformed

